/*
Topic: Basics
Program: Escape Sequences in Java

Definition:
Escape sequences are special characters used inside strings.

Common Escape Sequences:
1. \n  -> New Line
2. \t  -> Tab Space
3. \"  -> Double Quotes
4. \\  -> Backslash

Approach:
Using different escape sequences inside print statements.
*/

package basics;
public class H_EscapeSequences {
    public static void main(String[] args) {

        // New Line
        System.out.println("Hello\nWorld");

        // Tab Space
        System.out.println("Java\tProgramming");

        // Double Quotes
        System.out.println("\"Welcome to Java\"");

        // Backslash
        System.out.println("C:\\Users\\Divya");
    }
}